    function send_message() {
      if (!document.message_form.name.value) {
        alert('이름 또는 회사명을 입력해주세요.');
        document.message_form.name.focus();
        return;
      }
      if (!document.message_form.email.value) {
        alert('이메일을 입력해주세요.');
        document.message_form.email.focus();
        return;
      }
      if (!document.message_form.message.value) {
        alert('메세지를 입력해주세요.');
        document.message_form.message.focus();
        return;
      }
      document.message_form.submit();
    }
